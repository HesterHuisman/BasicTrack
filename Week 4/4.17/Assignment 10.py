print(3 % 4 == 0)  # False because should be 3
print(3 % 4 == 3)  # True because 3 cannot be divided by 4, so 3 remains
print(3 / 4 == 0)  # False because not exactly zero
print(3 // 4 == 0)  # True because rounded to integers it zero
print(3+4 * 2 == 14)  # False because 3 + 8 = 11
print(4-2+2 == 0)  # False because 4-2 = 2; 2+2 = 4
print(len("hello, world!") == 13)  # True because there are 13 characters in hello world
